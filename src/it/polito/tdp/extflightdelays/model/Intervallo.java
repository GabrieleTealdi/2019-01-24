package it.polito.tdp.extflightdelays.model;

public class Intervallo {
	

	private double i;
	private double f;
	
	public Intervallo(double i, double f) {
		super();
		this.i = i;
		this.f = f;
	}
	public double getI() {
		return i;
	}
	public void setI(double i) {
		this.i = i;
	}
	public double getF() {
		return f;
	}
	public void setF(double f) {
		this.f = f;
	}
	

}
